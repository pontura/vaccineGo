using System;

namespace GEX.graphics
{
	public class XRect
	{
        public int x, y, width, height;

        public XRect(int x = 0, int y = 0, int width = 0, int height = 0)
        {
            this.x =        x;
            this.y =        y;
            this.width =    width;
            this.height =   height;
        }

	}
}
